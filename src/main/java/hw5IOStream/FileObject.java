package hw5IOStream;

public class FileObject {
private Integer val1;
private Integer val2;
private Integer val3;

    public FileObject(Integer val1, Integer val2, Integer val3) {
        this.val1 = val1;
        this.val2 = val2;
        this.val3 = val3;
    }

    public Integer getVal1() {
        return val1;
    }


    public Integer getVal2() {
        return val2;
    }



    public Integer getVal3() {
        return val3;
    }


}
